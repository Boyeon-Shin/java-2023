package ch02;

public class PrintEx {
	public static void main(String[] args) {
		System.out.printf("%d", 123);
		System.out.println();
		System.out.printf("%d\n" ,123);
		System.out.printf("%5d\n" ,123);
		System.out.printf("%05d\n" ,123);
		System.out.println();
		
		System.out.printf("%d / %d = %.2f\n", 100, 200, 0.5);
	}
}
